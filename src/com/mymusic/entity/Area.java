package com.mymusic.entity;
/**
 * 地区类
 * @author 
 *
 */
public class Area {
	//地区编号
	private int a_id;
	//地区名称
	private String a_name;
	
	public Area() {
		super();
	}

	public int getA_id() {
		return a_id;
	}

	public void setA_id(int a_id) {
		this.a_id = a_id;
	}

	public String getA_name() {
		return a_name;
	}

	public void setA_name(String a_name) {
		this.a_name = a_name;
	}
	
}
